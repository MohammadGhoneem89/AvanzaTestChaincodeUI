package Model;

public class TransferParamObject {

	private String sender;
	private String receiver;
	private double balanceTT;
	
	
	
	public String getSender() {
		return sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	public String getReceiver() {
		return receiver;
	}
	public void setReceiver(String receiver) {
		this.receiver = receiver;
	}
	public double getBalanceTT() {
		return balanceTT;
	}
	public void setBalanceTT(double balanceTT) {
		this.balanceTT = balanceTT;
	}
	
	
}
